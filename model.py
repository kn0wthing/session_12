    model = load_model("gpt-config.pth")
    
    prompt = "Once upon a time"
    generated = generate_text(
        text=prompt,
        model=model,
        max_length=50,
        num_return_sequences=3
    )
    
    print("Generated sequences:")
    for i, text in enumerate(generated, 1):
        print(f"\n{i}. {text}") 